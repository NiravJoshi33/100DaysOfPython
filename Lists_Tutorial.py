#Lists Tutorial

name_of_faang = ["facebook","amazon","apple","netflix","google"]

print(name_of_faang[0]) #index 0 gives 1st element of the array
print(name_of_faang[-1]) #index -1 gives last element of the array

name_of_faang[-1] = "my company" #elements can be changed using the asignment

print(name_of_faang)

name_of_faang.append("Nirav.org") #append function adds a single item at the end of the array

print(name_of_faang)

name_of_faang.extend(["company1", "company2"]) #extend is used when an array is added to array

print(name_of_faang)